function findPosition(array $map, string $number){
    $xPosition;
    $yPosition;
    $position=[];
    foreach ($map as $yKey=>$colonne){
        foreach ($colonne as $xKey=>$ligne) {
            if ($ligne===$number){
                $xPosition=$xKey;
                $yPosition=$yKey;
            }
        }
    }
    $position=[$xPosition, $yPosition];
    return $position;
}

function moveRight(array $map, int $xPosition, int $yPosition){
    $map[$yPosition][$xPosition]="0";
    $map[$yPosition][$xPosition+1]="1";
    return $map;
}
function moveLeft(array $map, int $xPosition, int $yPosition){
    $map[$yPosition][$xPosition]="0";
    $map[$yPosition][$xPosition-1]="1";
    return $map;
}
function moveUp(array $map, int $xPosition, int $yPosition){
    $map[$yPosition][$xPosition]="0";
    $map[$yPosition-1][$xPosition]="1";
    return $map;
}
function moveDown(array $map, int $xPosition, int $yPosition){
    $map[$yPosition][$xPosition]="0";
    $map[$yPosition+1][$xPosition+1]="1";
    return $map;
}


